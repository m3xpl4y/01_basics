package at.tamasanu.campus.basics;

public class PrintLetterWhileForLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 1; i++)
        {
            System.out.println("X   X");
        }
        for(int j = 0; j<1; j++)
        {
            System.out.println("XX XX");
        }
        for(int k = 0; k < 1; k++)
        {
            System.out.println("X X X");
        }
        for(int l = 0; l < 1; l++)
        {
            System.out.println("X   X");
        }
        for(int m = 0; m < 1; m++)
        {
            System.out.println("X   X");
        }

    }
}
